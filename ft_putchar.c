#include "libft.h"

void	ft_putchar(wchar_t c)
{
	unsigned char s[3];
	if (c > 127)
	{
		s[0] = 194;
		if (c - 127 > 61)
		{
			s[0] += 1;
			s[1] = 127 + c - 191;
		}
		else
			s[1] = c;
		s[2] = 0;
	}
	else
	{
		s[0] = c;
		s[1] = 0;
	}
	write(1, s, 2);
}
